





CREATE VIEW [dbo].[CategoryView]
AS
SELECT	MAIN.Id,
		MAIN.Name AS 'Category',
		MAIN.ParentId AS 'ParentId',
		ISNULL(SUB1.Id, 0) AS 'SubCategory1Id',
		ISNULL(SUB1.Name, '') AS 'SubCategory1',
		ISNULL(SUB1.ParentId, 0) AS 'SubCategory1ParentId',
		ISNULL(SUB2.Id, 0) AS 'SubCategory2Id',
		ISNULL(SUB2.Name, '') AS 'SubCategory2',
		ISNULL(SUB2.ParentId, 0) AS 'SubCategory2ParentId'


		
FROM			ActiveCategoriesView MAIN
LEFT OUTER JOIN ActiveCategoriesView SUB1 ON MAIN.Id = SUB1.ParentId
LEFT OUTER JOIN ActiveCategoriesView SUB2 ON SUB1.Id = SUB2.ParentId
WHERE	MAIN.ParentId IS NULL
GO
